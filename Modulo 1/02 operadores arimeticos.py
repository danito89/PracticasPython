#Operadores aritméticos.
"""Se utilizan para realizar operaciones matemáticas con dos expresiones numéricas.
Son: + ( suma ), - ( resta ), * ( multiplicación ), / ( división ), // ( división entera ), 
** ( potenciación ) y % ( resto de división entera )."""

#ejemplos

print(2 + 3) # Suma
#resultado 5

print(5 - 2) # Resta
#resultado 3

print(4 * 3) # Multiplicación
#resultado 12

print(10 / 2) # División
#resultado 5.0

print(10 // 3) # División entera
#resultado 3

print(10 / 3) # División con resultado decimal
#resultado 3.3333333333333335

print(2 ** 3) # Potenciación
#resultado 8

print(2 ** 0.5) # Raíz cuadrada
#resultado 1.4142135623730951

print(10 % 3) # Resto de división entera
#resultado 1

print(10 % 2) # Resto de división entera, (para saber si es par o impar)
#resultado 0

print(11 % 2) # Resto de división entera, (para saber si es par o impar)
#resultado 1


